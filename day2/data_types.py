# Printing the very first character only, from a string - called "Subscripting"
print("Hello"[0])

# Printing the last character. Going "-2" will print a "l"
print("Hello"[-1])

# String
print("123"+"345") # Concatenating

# Integer
print(123+345)

# will print 123456789 ("_" is for better visibility)
print(123_456_789)

# Float
print(3.14159)

# Bolean
print(True)
print(False)