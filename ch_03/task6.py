#!/usr/bin/env python

def average(grade1, grade2, grade3):
    """
    Specify the grade between 0 and 100
    :param grade1: number
    :param grade2: number
    :param grade3: number
    :return: number
    """
    return (grade1 + grade2 + grade3) / 3

print(average(30, 30, 30))
print(average(20,17,38))
